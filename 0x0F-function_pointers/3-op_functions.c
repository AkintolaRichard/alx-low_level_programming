#include "3-calc.h"

/**
 * op_add - sums two integers numbers
 * @a: first number
 * @b: second number
 *
 * Return: the sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtracts two integers numbers
 * @a: first number
 * @b: second number
 *
 * Return: the difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies two integers numbers
 * @a: first number
 * @b: second number
 *
 * Return: the multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides two integers numbers
 * @a: first number
 * @b: second number
 *
 * Return: the integer division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - the remainder of the division two integers numbers
 * @a: first number
 * @b: second number
 *
 * Return: the remainter of the division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
